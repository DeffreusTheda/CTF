def headd(nodes):
    # Track which nodes are pointed to by others
    pointed_to = set()
    
    # Collect nodes that are pointed to
    for k, v in nodes.items():
        pointed_to.add(v)
    
    # Any node not pointed to could be a head
    possible_heads = [node for node in nodes if node not in pointed_to]
    
    return possible_heads

heads = headd(nodes)
