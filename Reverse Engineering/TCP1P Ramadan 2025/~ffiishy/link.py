for head in heads:
    links = [head]
    origin = head
    cnt = 1
    print(f'traversing head {head}...')
    while head in nodes.keys():
        head = nodes[head]
        cnt += 1
        links.insert(0, head)
        print(end=f' -> {head}')
    print(f'\ntail of {origin} is {head} with {cnt} links')
    rev = 'x = 133713371337\n'
    for link in links:
        rev += f'x = calc{link}(x, {link});\n'
    print(f'\n\nreversal: {rev}\n\n')

